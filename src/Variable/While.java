package Variable;

public class While {

	public static void main(String[] args) {
		int var1=5;
		while(var1<=9)
		{
			System.out.println(var1+" "+ "Java");
			var1++;
		}
		System.out.println("***********************");
		while(var1>=-9)
		{
			System.out.println(var1+" "+ "Testing");
			var1=var1-3;
			
		}
	}

}
